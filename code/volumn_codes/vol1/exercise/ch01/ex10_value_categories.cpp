/**
 * @file ex10_value_categories.cpp
 * @brief 练习：分类判断 — 值类别
 *
 * 判断给定表达式是左值（lvalue）还是右值（rvalue），
 * 并用表格形式打印结果。
 *
 * 判断方法：能否对该表达式取地址 &expr。
 * 能取地址的是左值，不能取地址的是右值。
 */

#include <iostream>
#include <string>

int main() {
    std::cout << "===== ex10: 值类别分类判断 =====\n\n";

    int x = 5;
    int* ptr = &x;

    std::cout << "表达式           | 值类别 | 原因\n";
    std::cout << "-----------------|--------|----------------------------------------\n";

    // 1. x — 左值
    // &x 合法，x 有名字和确定地址
    (void)&x;
    std::cout << "x                | 左值   | 有名字，有地址，可以取 &x\n";

    // 2. x + 3 — 右值
    // &(x + 3) 编译错误：算术表达式的结果是临时值
    // &(x + 3);  // 错误
    std::cout << "x + 3            | 右值   | 算术结果为临时值，无法取地址\n";

    // 3. "hello" — 左值
    // 字符串字面量是左值，它存储在静态存储区，有确定地址
    (void)"hello";
    (void)&"hello";
    std::cout << "\"hello\"          | 左值   | 字符串字面量有静态存储地址\n";

    // 4. *ptr — 左值
    // &(*ptr) 合法，解引用返回所指向的对象
    (void)&(*ptr);
    std::cout << "*ptr             | 左值   | 解引用返回引用，指向 x 的内存\n";

    // 5. x++ — 右值（后置递增）
    // &(x++) 编译错误：后置 ++ 返回旧值的副本（临时值）
    // &(x++);  // 错误
    std::cout << "x++              | 右值   | 后置递增返回旧值副本（临时值）\n";

    // 6. ++x — 左值（前置递增）
    // &(++x) 合法：前置 ++ 返回引用，是左值
    (void)&(++x);
    std::cout << "++x              | 左值   | 前置递增返回引用，是左值\n";

    std::cout << "\n===== 总结 =====\n";
    std::cout << "判断要点：能否对表达式取地址（&expr）\n";
    std::cout << "  - 能取地址 -> 左值（有名字、有地址、生命周期长）\n";
    std::cout << "  - 不能取地址 -> 右值（临时值、表达式结束后可能消失）\n";
    std::cout << "特别注意：\n";
    std::cout << "  - 字符串字面量是左值（静态存储区）\n";
    std::cout << "  - x++（后置）是右值，++x（前置）是左值\n";

    return 0;
}
